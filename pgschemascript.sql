 PASSFAIL,
 COMMENTS,
 OPERATOR_ID,
 WORKSTATION_NAME,
 TIME_STAMP,
 RECENT_FLAG,
 LINE_ID,
 CREATION_DATE,
 CREATED_BY,
 LAST_UPDATE_DATE,
 LAST_UPDATED_BY,
 DISPATCH_OPERATION_ID,
 OPERATION_SEQ_NUM)
 VALUES
 (P_ITEM_NUMBER,
 P_SERIAL_NUMBER,
 V_OPERATION_TYPE,
 V_OPERATION_DESC,
 'OK',
