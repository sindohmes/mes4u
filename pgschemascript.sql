 V_BARCODE_INDICATOR,
 V_LOCATION,
 V_LENGTH,
 V_OPERATION_ID,
 V_OPERATION_TYPE,
 V_OPERATION_DESC,
 V_OPERATION_SEQ,
 V_UNIQUE_CHECK
 FROM JOB_DISPATCH_OPERATIONS JDO
 WHERE JDO.DISPATCH_ID = V_DISPATCH_ID
 AND JDO.OPERATION_ID = P_OPERATION_ID
 AND JDO.DISABLE_DATE IS NULL;
