 NOW(),
 P_USER_ID,
 NOW(),
 P_USER_ID,
 V_OPERATION_ID,
 V_OPERATION_SEQ) RETURNING HISTORY_ID INTO V_HISTORY_ID ;

 INSERT INTO JOB_DISPATCH_OP_HIS_DETAILS
